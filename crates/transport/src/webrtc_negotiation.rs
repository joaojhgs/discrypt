/// SDP type carried through signaling.
#[derive(Clone, Copy, Debug, Eq, PartialEq, Serialize, Deserialize)]
#[serde(rename_all = "snake_case")]
pub enum WebRtcSdpType {
    /// SDP offer.
    Offer,
    /// SDP answer.
    Answer,
}

/// Serializable WebRTC session description for opaque signaling payloads.
#[derive(Clone, Eq, PartialEq, Serialize, Deserialize)]
pub struct WebRtcSessionDescription {
    /// Offer or answer.
    pub sdp_type: WebRtcSdpType,
    /// SDP body generated by the WebRTC stack.
    pub sdp: String,
}

impl fmt::Debug for WebRtcSessionDescription {
    fn fmt(&self, formatter: &mut fmt::Formatter<'_>) -> fmt::Result {
        formatter
            .debug_struct("WebRtcSessionDescription")
            .field("sdp_type", &self.sdp_type)
            .field("sdp", &"<redacted>")
            .finish()
    }
}

/// Serializable ICE candidate for trickle signaling.
#[derive(Clone, Eq, PartialEq, Serialize, Deserialize)]
pub struct WebRtcIceCandidate {
    /// Candidate attribute line.
    pub candidate: String,
    /// SDP media id, when supplied by the WebRTC stack.
    #[serde(default)]
    pub sdp_mid: Option<String>,
    /// SDP media line index, when supplied by the WebRTC stack.
    #[serde(default)]
    pub sdp_mline_index: Option<u16>,
    /// ICE username fragment, when supplied by the WebRTC stack.
    #[serde(default)]
    pub username_fragment: Option<String>,
    /// Candidate source URL, when supplied by the WebRTC stack.
    #[serde(default)]
    pub url: Option<String>,
}

impl fmt::Debug for WebRtcIceCandidate {
    fn fmt(&self, formatter: &mut fmt::Formatter<'_>) -> fmt::Result {
        formatter
            .debug_struct("WebRtcIceCandidate")
            .field("candidate", &"<redacted>")
            .field("sdp_mid", &self.sdp_mid)
            .field("sdp_mline_index", &self.sdp_mline_index)
            .field(
                "username_fragment",
                &self.username_fragment.as_ref().map(|_| "<redacted>"),
            )
            .field("url", &self.url.as_ref().map(|_| "<redacted>"))
            .finish()
    }
}

impl WebRtcIceCandidate {
    /// True when the candidate is relay/TURN-derived according to WebRTC candidate metadata.
    #[must_use]
    pub fn is_turn_relay_candidate(&self) -> bool {
        self.candidate
            .split_whitespace()
            .collect::<Vec<_>>()
            .windows(2)
            .any(|pair| pair == ["typ", "relay"])
            || self
                .url
                .as_deref()
                .is_some_and(|url| url.starts_with("turn:") || url.starts_with("turns:"))
    }
}

/// Encrypted negotiation payload kind carried as opaque signaling bytes.
#[derive(Clone, Copy, Debug, Eq, PartialEq, Serialize, Deserialize)]
#[serde(rename_all = "snake_case")]
pub enum WebRtcNegotiationPayloadKind {
    /// Sealed SDP offer.
    Offer,
    /// Sealed SDP answer.
    Answer,
    /// Sealed trickled ICE candidate.
    Candidate,
}

/// AES-GCM sealed WebRTC negotiation payload safe to hand to the signaling service.
#[derive(Clone, Eq, PartialEq, Serialize, Deserialize)]
pub struct SealedWebRtcNegotiationPayload {
    /// Wire format version.
    pub version: u8,
    /// Payload kind used as authenticated context.
    pub kind: WebRtcNegotiationPayloadKind,
    /// 96-bit AES-GCM nonce.
    pub nonce: [u8; 12],
    /// Ciphertext plus authentication tag.
    pub ciphertext: Vec<u8>,
}

impl fmt::Debug for SealedWebRtcNegotiationPayload {
    fn fmt(&self, formatter: &mut fmt::Formatter<'_>) -> fmt::Result {
        formatter
            .debug_struct("SealedWebRtcNegotiationPayload")
            .field("version", &self.version)
            .field("kind", &self.kind)
            .field("nonce", &"<redacted>")
            .field(
                "ciphertext",
                &format_args!("{} bytes", self.ciphertext.len()),
            )
            .finish()
    }
}

impl SealedWebRtcNegotiationPayload {
    /// Serialize sealed payload bytes for `SignalingClient::publish_signal`.
    pub fn to_opaque_bytes(&self) -> Result<Vec<u8>, TransportError> {
        serde_json::to_vec(self).map_err(|err| {
            TransportError::Unavailable(format!("encode sealed negotiation payload failed: {err}"))
        })
    }

    /// Decode sealed payload bytes returned by `SignalingClient::take_signals`.
    pub fn from_opaque_bytes(bytes: &[u8]) -> Result<Self, TransportError> {
        serde_json::from_slice(bytes).map_err(|err| {
            TransportError::Unavailable(format!("decode sealed negotiation payload failed: {err}"))
        })
    }
}

/// Symmetric sealer for WebRTC negotiation payloads before they enter signaling.
pub struct WebRtcNegotiationSealer {
    key: [u8; 32],
}

impl fmt::Debug for WebRtcNegotiationSealer {
    fn fmt(&self, formatter: &mut fmt::Formatter<'_>) -> fmt::Result {
        formatter
            .debug_struct("WebRtcNegotiationSealer")
            .field("key", &"<redacted>")
            .finish()
    }
}

impl WebRtcNegotiationSealer {
    /// Construct a sealer from a caller-supplied 256-bit room/session negotiation key.
    #[must_use]
    pub fn new(key: [u8; 32]) -> Self {
        Self { key }
    }

    /// Seal an SDP offer or answer.
    pub fn seal_description(
        &self,
        description: &WebRtcSessionDescription,
    ) -> Result<SealedWebRtcNegotiationPayload, TransportError> {
        let kind = match description.sdp_type {
            WebRtcSdpType::Offer => WebRtcNegotiationPayloadKind::Offer,
            WebRtcSdpType::Answer => WebRtcNegotiationPayloadKind::Answer,
        };
        self.seal_json(kind, description)
    }

    /// Open a sealed SDP offer or answer.
    pub fn open_description(
        &self,
        sealed: &SealedWebRtcNegotiationPayload,
    ) -> Result<WebRtcSessionDescription, TransportError> {
        match sealed.kind {
            WebRtcNegotiationPayloadKind::Offer | WebRtcNegotiationPayloadKind::Answer => {
                self.open_json(sealed)
            }
            WebRtcNegotiationPayloadKind::Candidate => Err(TransportError::Unavailable(
                "sealed candidate cannot be opened as SDP".to_owned(),
            )),
        }
    }

    /// Seal one trickled ICE candidate.
    pub fn seal_candidate(
        &self,
        candidate: &WebRtcIceCandidate,
    ) -> Result<SealedWebRtcNegotiationPayload, TransportError> {
        self.seal_json(WebRtcNegotiationPayloadKind::Candidate, candidate)
    }

    /// Open one sealed trickled ICE candidate.
    pub fn open_candidate(
        &self,
        sealed: &SealedWebRtcNegotiationPayload,
    ) -> Result<WebRtcIceCandidate, TransportError> {
        if sealed.kind != WebRtcNegotiationPayloadKind::Candidate {
            return Err(TransportError::Unavailable(
                "sealed SDP cannot be opened as candidate".to_owned(),
            ));
        }
        self.open_json(sealed)
    }

    fn seal_json<T: Serialize>(
        &self,
        kind: WebRtcNegotiationPayloadKind,
        value: &T,
    ) -> Result<SealedWebRtcNegotiationPayload, TransportError> {
        use aes_gcm::aead::{Aead, Payload};
        use aes_gcm::{Aes256Gcm, KeyInit, Nonce};
        use rand::RngCore;

        let mut nonce = [0u8; 12];
        rand::rngs::OsRng.fill_bytes(&mut nonce);
        let plaintext = serde_json::to_vec(value).map_err(|err| {
            TransportError::Unavailable(format!("encode negotiation payload failed: {err}"))
        })?;
        let cipher = Aes256Gcm::new_from_slice(&self.key).map_err(|err| {
            TransportError::Unavailable(format!("initialize negotiation sealer failed: {err}"))
        })?;
        let ciphertext = cipher
            .encrypt(
                Nonce::from_slice(&nonce),
                Payload {
                    msg: &plaintext,
                    aad: aad_for_kind(kind).as_bytes(),
                },
            )
            .map_err(|err| {
                TransportError::Unavailable(format!("seal negotiation payload failed: {err}"))
            })?;
        Ok(SealedWebRtcNegotiationPayload {
            version: 1,
            kind,
            nonce,
            ciphertext,
        })
    }

    fn open_json<T: for<'de> Deserialize<'de>>(
        &self,
        sealed: &SealedWebRtcNegotiationPayload,
    ) -> Result<T, TransportError> {
        use aes_gcm::aead::{Aead, Payload};
        use aes_gcm::{Aes256Gcm, KeyInit, Nonce};
        if sealed.version != 1 {
            return Err(TransportError::Unavailable(
                "unsupported sealed negotiation payload version".to_owned(),
            ));
        }
        let cipher = Aes256Gcm::new_from_slice(&self.key).map_err(|err| {
            TransportError::Unavailable(format!("initialize negotiation opener failed: {err}"))
        })?;
        let plaintext = cipher
            .decrypt(
                Nonce::from_slice(&sealed.nonce),
                Payload {
                    msg: &sealed.ciphertext,
                    aad: aad_for_kind(sealed.kind).as_bytes(),
                },
            )
            .map_err(|err| {
                TransportError::Unavailable(format!("open negotiation payload failed: {err}"))
            })?;
        serde_json::from_slice(&plaintext).map_err(|err| {
            TransportError::Unavailable(format!("decode negotiation payload failed: {err}"))
        })
    }
}

fn aad_for_kind(kind: WebRtcNegotiationPayloadKind) -> &'static str {
    match kind {
        WebRtcNegotiationPayloadKind::Offer => "discrypt-webrtc-negotiation-v1:offer",
        WebRtcNegotiationPayloadKind::Answer => "discrypt-webrtc-negotiation-v1:answer",
        WebRtcNegotiationPayloadKind::Candidate => "discrypt-webrtc-negotiation-v1:candidate",
    }
}

