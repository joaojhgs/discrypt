impl WebRtcSessionDescription {
    fn from_rtc(description: RTCSessionDescription) -> Result<Self, TransportError> {
        let sdp_type = match description.sdp_type {
            RTCSdpType::Offer => WebRtcSdpType::Offer,
            RTCSdpType::Answer => WebRtcSdpType::Answer,
            other => {
                return Err(TransportError::Unavailable(format!(
                    "unsupported SDP type for Discrypt negotiation: {other:?}"
                )));
            }
        };
        Ok(Self {
            sdp_type,
            sdp: description.sdp,
        })
    }

    fn into_rtc(self) -> Result<RTCSessionDescription, TransportError> {
        let rtc = match self.sdp_type {
            WebRtcSdpType::Offer => RTCSessionDescription::offer(self.sdp),
            WebRtcSdpType::Answer => RTCSessionDescription::answer(self.sdp),
        }
        .map_err(|err| TransportError::Unavailable(format!("parse remote SDP failed: {err}")))?;
        Ok(rtc)
    }
}

impl WebRtcIceCandidate {
    fn into_rtc(self) -> RTCIceCandidateInit {
        RTCIceCandidateInit {
            candidate: self.candidate,
            sdp_mid: self.sdp_mid,
            sdp_mline_index: self.sdp_mline_index,
            username_fragment: self.username_fragment,
            url: self.url,
        }
    }
}

#[derive(Clone)]
struct CandidateCollector {
    candidates: Arc<Mutex<Vec<WebRtcIceCandidate>>>,
    metrics: Arc<Mutex<DirectPathMetricsState>>,
    data_channels: DataChannelHub,
}

#[async_trait]
impl PeerConnectionEventHandler for CandidateCollector {
    async fn on_data_channel(&self, channel: Arc<dyn DataChannel>) {
        self.data_channels.attach(channel).await;
    }

    async fn on_ice_gathering_state_change(&self, state: RTCIceGatheringState) {
        self.metrics.lock().await.ice_gathering_state = state;
    }

    async fn on_ice_connection_state_change(&self, state: RTCIceConnectionState) {
        self.metrics.lock().await.ice_connection_state = state;
    }

    async fn on_connection_state_change(&self, state: RTCPeerConnectionState) {
        self.metrics.lock().await.peer_connection_state = state;
    }

    async fn on_ice_candidate(&self, event: webrtc::peer_connection::RTCPeerConnectionIceEvent) {
        match event.candidate.to_json() {
            Ok(init) => {
                let candidate = WebRtcIceCandidate {
                    candidate: init.candidate,
                    sdp_mid: init.sdp_mid,
                    sdp_mline_index: init.sdp_mline_index,
                    username_fragment: init.username_fragment,
                    url: init.url,
                };
                let is_relay = candidate.is_turn_relay_candidate();
                self.candidates.lock().await.push(candidate);
                let mut metrics = self.metrics.lock().await;
                metrics.local_candidates_gathered += 1;
                if is_relay {
                    metrics.local_relay_candidates_gathered += 1;
                }
            }
            Err(_err) => {}
        }
    }
}

fn to_rtc_ice_servers(config: &IceServerConfig) -> Vec<RTCIceServer> {
    let mut servers = Vec::new();
    for endpoint in &config.stun_servers {
        servers.push(RTCIceServer {
            urls: vec![endpoint.0.clone()],
            username: String::new(),
            credential: String::new(),
        });
    }
    for server in &config.turn_servers {
        servers.push(RTCIceServer {
            urls: vec![server.endpoint.0.clone()],
            username: server.username.clone().unwrap_or_default(),
            credential: server.credential.clone().unwrap_or_default(),
        });
    }
    servers
}

