#[derive(Clone)]
struct DataChannelHub {
    label: String,
    channels: Arc<Mutex<Vec<Arc<dyn DataChannel>>>>,
    inbound_tx: mpsc::Sender<Vec<u8>>,
    inbound_rx: Arc<Mutex<mpsc::Receiver<Vec<u8>>>>,
    metrics: Arc<Mutex<DataTransportMetricsState>>,
    open_notify: Arc<Notify>,
}

impl DataChannelHub {
    fn new(label: String) -> Self {
        let (inbound_tx, inbound_rx) = mpsc::channel(256);
        Self {
            metrics: Arc::new(Mutex::new(DataTransportMetricsState::new(label.clone()))),
            label,
            channels: Arc::new(Mutex::new(Vec::new())),
            inbound_tx,
            inbound_rx: Arc::new(Mutex::new(inbound_rx)),
            open_notify: Arc::new(Notify::new()),
        }
    }

    async fn attach(&self, channel: Arc<dyn DataChannel>) {
        let label = channel.label().await.unwrap_or_else(|_| self.label.clone());
        if label != self.label {
            return;
        }
        {
            let mut channels = self.channels.lock().await;
            channels.push(Arc::clone(&channel));
            let mut metrics = self.metrics.lock().await;
            metrics.attached_channels = channels.len() as u64;
            metrics.last_state = "attached".to_owned();
        }
        let inbound_tx = self.inbound_tx.clone();
        let metrics = Arc::clone(&self.metrics);
        let open_notify = Arc::clone(&self.open_notify);
        tokio::spawn(async move {
            while let Some(event) = channel.poll().await {
                match event {
                    DataChannelEvent::OnOpen => {
                        let mut metrics = metrics.lock().await;
                        metrics.open = true;
                        metrics.last_state = "open".to_owned();
                        drop(metrics);
                        open_notify.notify_waiters();
                    }
                    DataChannelEvent::OnMessage(message) => {
                        let bytes = message.data.to_vec();
                        {
                            let mut metrics = metrics.lock().await;
                            metrics.frames_received = metrics.frames_received.saturating_add(1);
                            metrics.bytes_received =
                                metrics.bytes_received.saturating_add(bytes.len() as u64);
                            metrics.last_state = "message".to_owned();
                        }
                        if inbound_tx.send(bytes).await.is_err() {
                            break;
                        }
                    }
                    DataChannelEvent::OnClose => {
                        let mut metrics = metrics.lock().await;
                        metrics.open = false;
                        metrics.last_state = "closed".to_owned();
                        break;
                    }
                    DataChannelEvent::OnClosing => {
                        metrics.lock().await.last_state = "closing".to_owned();
                    }
                    DataChannelEvent::OnError => {
                        metrics.lock().await.last_state = "error".to_owned();
                    }
                    DataChannelEvent::OnBufferedAmountLow => {
                        metrics.lock().await.last_state = "buffered_amount_low".to_owned();
                    }
                    DataChannelEvent::OnBufferedAmountHigh => {
                        metrics.lock().await.last_state = "buffered_amount_high".to_owned();
                    }
                }
            }
        });
    }

    async fn wait_open(&self, duration: Duration) -> Result<(), TransportError> {
        if self.metrics.lock().await.open {
            return Ok(());
        }
        timeout(duration, self.open_notify.notified())
            .await
            .map_err(|_| TransportError::Unavailable("DataChannel did not open".to_owned()))?;
        Ok(())
    }

    async fn send(&self, frame: Vec<u8>) -> Result<(), TransportError> {
        if frame.is_empty() {
            return Err(TransportError::Unavailable(
                "text/control data frame is empty".to_owned(),
            ));
        }
        self.wait_open(Duration::from_secs(10)).await?;
        let channels = self.channels.lock().await;
        let channel = channels
            .first()
            .cloned()
            .ok_or_else(|| TransportError::Unavailable("no DataChannel attached".to_owned()))?;
        drop(channels);
        if channel.ready_state().await.map_err(data_channel_error)? != RTCDataChannelState::Open {
            return Err(TransportError::Unavailable(
                "DataChannel is not open".to_owned(),
            ));
        }
        let sent_len = frame.len();
        channel
            .send(BytesMut::from(frame.as_slice()))
            .await
            .map_err(data_channel_error)?;
        let mut metrics = self.metrics.lock().await;
        metrics.frames_sent = metrics.frames_sent.saturating_add(1);
        metrics.bytes_sent = metrics.bytes_sent.saturating_add(sent_len as u64);
        metrics.last_state = "sent".to_owned();
        Ok(())
    }

    async fn recv(&self) -> Result<Vec<u8>, TransportError> {
        let mut inbound_rx = self.inbound_rx.lock().await;
        inbound_rx
            .recv()
            .await
            .ok_or_else(|| TransportError::Unavailable("DataChannel receive closed".to_owned()))
    }

    async fn snapshot(&self) -> WebRtcDataTransportMetrics {
        self.metrics.lock().await.snapshot()
    }
}

fn data_channel_error(error: impl fmt::Display) -> TransportError {
    TransportError::Unavailable(format!("WebRTC DataChannel failed: {error}"))
}

/// Direct ICE/WebRTC state and metrics visible to app-service adapters.
#[derive(Clone, Debug, Eq, PartialEq, Serialize, Deserialize)]
pub struct WebRtcDirectPathMetrics {
    /// Snapshot schema version for app-service/Tauri consumers.
    pub schema_version: u16,
    /// Current aggregate peer connection state.
    pub peer_connection_state: String,
    /// Current ICE connection state.
    pub ice_connection_state: String,
    /// Current ICE gathering state.
    pub ice_gathering_state: String,
    /// Total local candidates gathered by the WebRTC stack.
    pub local_candidates_gathered: u64,
    /// Total remote trickle candidates applied to this peer.
    pub remote_candidates_applied: u64,
    /// Configured TURN server count for fallback path availability.
    pub configured_turn_servers: u64,
    /// Locally gathered relay/TURN candidates.
    pub local_relay_candidates_gathered: u64,
    /// Remote relay/TURN candidates applied through trickle ICE.
    pub remote_relay_candidates_applied: u64,
    /// True only when WebRTC reports a direct ICE-capable connected/completed state.
    pub direct_path_ready: bool,
    /// True only when WebRTC is connected and relay candidate evidence exists with TURN configured.
    pub turn_fallback_ready: bool,
}

impl WebRtcDirectPathMetrics {
    /// Current metrics schema.
    pub const SCHEMA_VERSION: u16 = 1;
}

#[derive(Clone, Debug)]
struct DirectPathMetricsState {
    peer_connection_state: RTCPeerConnectionState,
    ice_connection_state: RTCIceConnectionState,
    ice_gathering_state: RTCIceGatheringState,
    local_candidates_gathered: u64,
    remote_candidates_applied: u64,
    configured_turn_servers: u64,
    local_relay_candidates_gathered: u64,
    remote_relay_candidates_applied: u64,
}

impl Default for DirectPathMetricsState {
    fn default() -> Self {
        Self {
            peer_connection_state: RTCPeerConnectionState::New,
            ice_connection_state: RTCIceConnectionState::New,
            ice_gathering_state: RTCIceGatheringState::New,
            local_candidates_gathered: 0,
            remote_candidates_applied: 0,
            configured_turn_servers: 0,
            local_relay_candidates_gathered: 0,
            remote_relay_candidates_applied: 0,
        }
    }
}

impl DirectPathMetricsState {
    fn with_configured_turn_servers(configured_turn_servers: u64) -> Self {
        Self {
            configured_turn_servers,
            ..Self::default()
        }
    }

    fn snapshot(&self) -> WebRtcDirectPathMetrics {
        let connected = matches!(
            self.peer_connection_state,
            RTCPeerConnectionState::Connected
        ) || matches!(
            self.ice_connection_state,
            RTCIceConnectionState::Connected | RTCIceConnectionState::Completed
        );
        WebRtcDirectPathMetrics {
            schema_version: WebRtcDirectPathMetrics::SCHEMA_VERSION,
            peer_connection_state: self.peer_connection_state.to_string(),
            ice_connection_state: self.ice_connection_state.to_string(),
            ice_gathering_state: self.ice_gathering_state.to_string(),
            local_candidates_gathered: self.local_candidates_gathered,
            remote_candidates_applied: self.remote_candidates_applied,
            configured_turn_servers: self.configured_turn_servers,
            local_relay_candidates_gathered: self.local_relay_candidates_gathered,
            remote_relay_candidates_applied: self.remote_relay_candidates_applied,
            direct_path_ready: connected,
            turn_fallback_ready: connected
                && self.configured_turn_servers > 0
                && (self.local_relay_candidates_gathered > 0
                    || self.remote_relay_candidates_applied > 0),
        }
    }
}

/// Local WebRTC negotiation configuration.
#[derive(Clone, Debug, Eq, PartialEq, Serialize, Deserialize)]
pub struct WebRtcNegotiationConfig {
    /// Validated STUN/TURN configuration from invite/group policy.
    pub ice_servers: IceServerConfig,
    /// Local UDP bind addresses. Use `127.0.0.1:0` in deterministic tests.
    pub udp_addrs: Vec<String>,
    /// DataChannel label used to ensure SDP negotiates an application m-line.
    pub data_channel_label: String,
}

impl WebRtcNegotiationConfig {
    /// Build a config with a safe loopback bind default for tests/local embedded use.
    #[must_use]
    pub fn new(ice_servers: IceServerConfig) -> Self {
        Self {
            ice_servers,
            udp_addrs: vec!["127.0.0.1:0".to_owned()],
            data_channel_label: "discrypt-control".to_owned(),
        }
    }

    fn validate(&self, now: DateTime<Utc>) -> Result<(), TransportError> {
        self.ice_servers.validate_credentials_at(now)?;
        if self.udp_addrs.is_empty() {
            return Err(TransportError::Unavailable(
                "at least one UDP bind address is required for WebRTC negotiation".to_owned(),
            ));
        }
        if self.data_channel_label.trim().is_empty() {
            return Err(TransportError::Unavailable(
                "data channel label is required for WebRTC negotiation".to_owned(),
            ));
        }
        Ok(())
    }
}

/// WebRTC negotiation owner for one peer connection.
pub struct WebRtcNegotiator {
    peer_connection: Box<dyn PeerConnection>,
    candidates: Arc<Mutex<Vec<WebRtcIceCandidate>>>,
    metrics: Arc<Mutex<DirectPathMetricsState>>,
    data_channels: DataChannelHub,
    data_channel_label: String,
    turn_endpoints: Vec<Endpoint>,
}

impl WebRtcNegotiator {
    /// Create a WebRTC peer connection configured with validated ICE servers.
    pub async fn new(config: WebRtcNegotiationConfig) -> Result<Self, TransportError> {
        config.validate(Utc::now())?;
        let candidates = Arc::new(Mutex::new(Vec::new()));
        let metrics = Arc::new(Mutex::new(
            DirectPathMetricsState::with_configured_turn_servers(
                config.ice_servers.turn_servers.len() as u64,
            ),
        ));
        let data_channels = DataChannelHub::new(config.data_channel_label.clone());
        let handler = Arc::new(CandidateCollector {
            candidates: Arc::clone(&candidates),
            metrics: Arc::clone(&metrics),
            data_channels: data_channels.clone(),
        });
        let mut media = MediaEngine::default();
        media.register_default_codecs().map_err(|err| {
            TransportError::Unavailable(format!("register media codecs failed: {err}"))
        })?;
        let registry =
            register_default_interceptors(Registry::new(), &mut media).map_err(|err| {
                TransportError::Unavailable(format!("register WebRTC interceptors failed: {err}"))
            })?;
        let peer_connection = PeerConnectionBuilder::new()
            .with_configuration(
                RTCConfigurationBuilder::new()
                    .with_ice_servers(to_rtc_ice_servers(&config.ice_servers))
                    .build(),
            )
            .with_media_engine(media)
            .with_interceptor_registry(registry)
            .with_handler(handler)
            .with_udp_addrs(config.udp_addrs.clone())
            .build()
            .await
            .map_err(|err| {
                TransportError::Unavailable(format!("build WebRTC peer failed: {err}"))
            })?;
        Ok(Self {
            peer_connection: Box::new(peer_connection),
            candidates,
            metrics,
            data_channels,
            data_channel_label: config.data_channel_label,
            turn_endpoints: config
                .ice_servers
                .turn_servers
                .iter()
                .map(|server| server.endpoint.clone())
                .collect(),
        })
    }

    /// Create and set a local SDP offer. Candidate gathering starts after this call.
    pub async fn create_offer(&self) -> Result<WebRtcSessionDescription, TransportError> {
        let _channel = self
            .peer_connection
            .create_data_channel(&self.data_channel_label, None)
            .await
            .map_err(|err| {
                TransportError::Unavailable(format!("create data channel failed: {err}"))
            })?;
        self.data_channels.attach(_channel).await;
        let offer = self
            .peer_connection
            .create_offer(None)
            .await
            .map_err(|err| {
                TransportError::Unavailable(format!("create WebRTC offer failed: {err}"))
            })?;
        self.peer_connection
            .set_local_description(offer)
            .await
            .map_err(|err| TransportError::Unavailable(format!("set local offer failed: {err}")))?;
        let local = self
            .peer_connection
            .local_description()
            .await
            .ok_or_else(|| {
                TransportError::Unavailable("WebRTC local offer was not recorded".to_owned())
            })?;
        WebRtcSessionDescription::from_rtc(local)
    }

    /// Apply a remote offer, then create and set a local SDP answer.
    pub async fn create_answer(
        &self,
        remote_offer: WebRtcSessionDescription,
    ) -> Result<WebRtcSessionDescription, TransportError> {
        self.peer_connection
            .set_remote_description(remote_offer.into_rtc()?)
            .await
            .map_err(|err| {
                TransportError::Unavailable(format!("set remote offer failed: {err}"))
            })?;
        let answer = self
            .peer_connection
            .create_answer(None)
            .await
            .map_err(|err| {
                TransportError::Unavailable(format!("create WebRTC answer failed: {err}"))
            })?;
        self.peer_connection
            .set_local_description(answer)
            .await
            .map_err(|err| {
                TransportError::Unavailable(format!("set local answer failed: {err}"))
            })?;
        let local = self
            .peer_connection
            .local_description()
            .await
            .ok_or_else(|| {
                TransportError::Unavailable("WebRTC local answer was not recorded".to_owned())
            })?;
        WebRtcSessionDescription::from_rtc(local)
    }

    /// Apply a remote answer to an offer-side peer connection.
    pub async fn accept_answer(
        &self,
        remote_answer: WebRtcSessionDescription,
    ) -> Result<(), TransportError> {
        self.peer_connection
            .set_remote_description(remote_answer.into_rtc()?)
            .await
            .map_err(|err| TransportError::Unavailable(format!("set remote answer failed: {err}")))
    }

    /// Apply one trickled remote ICE candidate.
    pub async fn add_remote_candidate(
        &self,
        candidate: WebRtcIceCandidate,
    ) -> Result<(), TransportError> {
        if candidate.is_turn_relay_candidate() {
            self.metrics.lock().await.remote_relay_candidates_applied += 1;
        }
        self.peer_connection
            .add_ice_candidate(candidate.into_rtc())
            .await
            .map_err(|err| {
                TransportError::Unavailable(format!("add ICE candidate failed: {err}"))
            })?;
        self.metrics.lock().await.remote_candidates_applied += 1;
        Ok(())
    }

    /// Drain locally gathered ICE candidates in callback order for signaling.
    pub async fn drain_local_candidates(&self) -> Vec<WebRtcIceCandidate> {
        let mut candidates = self.candidates.lock().await;
        std::mem::take(&mut *candidates)
    }

    /// Current direct ICE/WebRTC metrics for app-service/Tauri state surfaces.
    pub async fn direct_path_metrics(&self) -> WebRtcDirectPathMetrics {
        self.metrics.lock().await.snapshot()
    }

    /// Select a direct route in a transport session only after WebRTC reports readiness.
    pub async fn select_direct_route_if_ready(
        &self,
        session: &mut TransportSession,
        endpoint: Endpoint,
    ) -> Result<Option<TransportSessionSnapshot>, TransportError> {
        if !self.direct_path_metrics().await.direct_path_ready {
            return Ok(None);
        }
        session
            .select_direct(endpoint)
            .map(Some)
            .map_err(Into::into)
    }

    /// Select a TURN relay route only after WebRTC reports relay candidate evidence.
    pub async fn select_turn_route_if_ready(
        &self,
        session: &mut TransportSession,
    ) -> Result<Option<TransportSessionSnapshot>, TransportError> {
        if !self.direct_path_metrics().await.turn_fallback_ready {
            return Ok(None);
        }
        let endpoint = self.turn_endpoints.first().cloned().ok_or_else(|| {
            TransportError::Unavailable("TURN fallback has no endpoint".to_owned())
        })?;
        session
            .select_turn_relay(endpoint)
            .map(Some)
            .map_err(Into::into)
    }

    /// Wait for the app-facing text/control DataChannel to open.
    pub async fn wait_text_control_transport_ready(
        &self,
        duration: Duration,
    ) -> Result<(), TransportError> {
        self.data_channels.wait_open(duration).await
    }

    /// Close the peer connection.
    pub async fn close(&self) -> Result<(), TransportError> {
        self.peer_connection
            .close()
            .await
            .map_err(|err| TransportError::Unavailable(format!("close WebRTC peer failed: {err}")))
    }
}

